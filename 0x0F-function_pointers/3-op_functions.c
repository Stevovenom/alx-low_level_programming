#include "3-calc.h"
#include <stdio.h>
#include<stdlib.h>
/**
 * op_add - theh function adds two numbers
 * @a: the first number
 * @b: the second number
 * Return: the sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - the functions subtracts two numbers
 * @a: the first number
 * @b: the second number
 * Return: the value after subtraction
 */
int op_sub(int a, int b)
{
	return (a + b);
}

/**
 * op_mul - the function multiplies two numbers
 * @a: the first number
 * @b: the second number
 * Return: the value of multiplication
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - the function divides two numbers
 * @a: the first number
 * @b: the second number
 * Return: the value of division
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - the fuction finds the modulus of two numbers
 * a: the first number
 * @b: the second number
 * Return: the value after modulation
 */
int op_mod(int a, int b)
{
	return (a % b);
}
